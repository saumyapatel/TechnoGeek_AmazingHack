package com.amazon.dataObjects;

public class Subscription {

	String subs_id = "";
	String subs_attr = "";
	String subs_op = "";
	String subs_val = "";


	public String getSubs_attr() {
		return subs_attr;
	}
	public void setSubs_attr(String subs_attr) {
		this.subs_attr = subs_attr;
	}
	public String getSubs_op() {
		return subs_op;
	}
	public void setSubs_op(String subs_op) {
		this.subs_op = subs_op;
	}
	public String getSubs_val() {
		return subs_val;
	}
	public void setSubs_val(String subs_val) {
		this.subs_val = subs_val;
	}
	public String getSubs_id() {
		return subs_id;
	}
	public void setSubs_id(String subs_id) {
		this.subs_id = subs_id;
	}
}
